#include "push_swap.h"

int	ft_isnum(char *s)
{
	int	i;
	
	i = 0;
	if (s[0] == '-' || s[i] == '+')
		i++;
	while (s[i])
	{
		if (s[i] == '.' || (s[i] <= '9' && s[i] >= '0'))
			i++;
		else
			return (-1);
	}
	return (i);
}
